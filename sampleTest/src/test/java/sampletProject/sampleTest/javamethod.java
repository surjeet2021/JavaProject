package sampletProject.sampleTest;

public class javamethod {
	
	static int addtion(int a,int b) {
		
		return a+b;
	}
	
	static int subtruction(int a, int b) {
		
		return a-b;
		
	}
	
	public static void main(String[] args) {
		
		System.out.println(addtion(2,4));
		System.out.println(subtruction(33,10));
	}

}


